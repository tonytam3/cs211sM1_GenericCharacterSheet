
public class StatNode {
	private String StatName;
	private int StatValue;
	
	public String getStatName() {
		return StatName;
	}
	public void setStatName(String statName) {
		StatName = statName;
	}
	public int getStatValue() {
		return StatValue;
	}
	public void setStatValue(int statValue) {
		StatValue = statValue;
	}
}
